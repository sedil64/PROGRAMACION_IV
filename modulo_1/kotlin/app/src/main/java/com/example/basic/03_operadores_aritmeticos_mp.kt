package com.example.basics

fun main(){
    println("Operadores Aritmeticos")
    val golesEquipo1: Double = 4.0
    val golesEquipo2: Double = 2.0

    println("Suma total: ${golesEquipo1 + golesEquipo2}")
    println("Diferencia: ${golesEquipo1 - golesEquipo2}")
    println("Multiplicacion: ${golesEquipo1 * golesEquipo2}")
    println("Division: ${golesEquipo1 / golesEquipo2}")

}